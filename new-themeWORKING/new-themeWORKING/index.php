                    }
                    const scrollY = scrollTarget.getBoundingClientRect().top + window.pageYOffset - headerHeight;
                    window.scrollTo({
                        top: scrollY,
                        behavior: 'smooth'
                    });
                    // Close mobile nav if open
                    document.getElementById('navbar').classList.remove('active');
                });
            });
